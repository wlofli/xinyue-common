package com.xinyue.manage.beans;

/**
 * 信贷经理管理-推荐列表内容(后台)
 * @author MK)茅
 * @version v1.0
 * @date 创建时间：2015年9月6日
 */
public class InvitationMemberInfo {

	//被推荐者姓名
	private String name = "";
	
	//被推荐者手机
	private String telPhone = "";
	
	//被推荐者类型
	private String memberType = "";
	
	//被推荐者注册时间
	private String registerTime = "";
	
	//积分
	private String point = "";
	
	//收益
	private String reward = "";
	
	//成功贷款时间
	private String successLoanTime = "";
	
	//充值奖励
	private String recharge = "";
	
	//充值时间
	private String rechargeTime = "";

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the telPhone
	 */
	public String getTelPhone() {
		return telPhone;
	}

	/**
	 * @param telPhone the telPhone to set
	 */
	public void setTelPhone(String telPhone) {
		this.telPhone = telPhone;
	}

	/**
	 * @return the memberType
	 */
	public String getMemberType() {
		return memberType;
	}

	/**
	 * @param memberType the memberType to set
	 */
	public void setMemberType(String memberType) {
		this.memberType = memberType;
	}

	/**
	 * @return the registerTime
	 */
	public String getRegisterTime() {
		return registerTime;
	}

	/**
	 * @param registerTime the registerTime to set
	 */
	public void setRegisterTime(String registerTime) {
		this.registerTime = registerTime;
	}

	/**
	 * @return the point
	 */
	public String getPoint() {
		return point;
	}

	/**
	 * @param point the point to set
	 */
	public void setPoint(String point) {
		this.point = point;
	}

	/**
	 * @return the reward
	 */
	public String getReward() {
		return reward;
	}

	/**
	 * @param reward the reward to set
	 */
	public void setReward(String reward) {
		this.reward = reward;
	}

	/**
	 * @return the successLoanTime
	 */
	public String getSuccessLoanTime() {
		return successLoanTime;
	}

	/**
	 * @param successLoanTime the successLoanTime to set
	 */
	public void setSuccessLoanTime(String successLoanTime) {
		this.successLoanTime = successLoanTime;
	}

	/**
	 * @return the recharge
	 */
	public String getRecharge() {
		return recharge;
	}

	/**
	 * @param recharge the recharge to set
	 */
	public void setRecharge(String recharge) {
		this.recharge = recharge;
	}

	/**
	 * @return the rechargeTime
	 */
	public String getRechargeTime() {
		return rechargeTime;
	}

	/**
	 * @param rechargeTime the rechargeTime to set
	 */
	public void setRechargeTime(String rechargeTime) {
		this.rechargeTime = rechargeTime;
	}
	
	
}
