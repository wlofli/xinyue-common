package com.xinyue.manage.beans;

/**
 * 信贷经理管理详情-客户信息
 * @author MK)茅
 * @version v1.0
 * @date 创建时间：2015年9月11日
 */
public class CustomerInfo {

	//订单编号
	private String orderId = "";
	
	//申请人
	private String applicant = "";
	
	//产品名称
	private String productName = "";
	
	//申请人电话
	private String applicantTel = "";
	
	//贷款额
	private String loanAmount = "";
	
	//贷款期限
	private String loanLimit = "";
	
	//贷款单
	private String company = "";
	
	//申请时间
	private String applicantDate = "";
	
	//状态
	private String orderStatus = "";
	
	//领取方式
	private String orderType = "";

	/**
	 * @return the orderId
	 */
	public String getOrderId() {
		return orderId;
	}

	/**
	 * @param orderId the orderId to set
	 */
	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}

	/**
	 * @return the applicant
	 */
	public String getApplicant() {
		return applicant;
	}

	/**
	 * @param applicant the applicant to set
	 */
	public void setApplicant(String applicant) {
		this.applicant = applicant;
	}

	/**
	 * @return the productName
	 */
	public String getProductName() {
		return productName;
	}

	/**
	 * @param productName the productName to set
	 */
	public void setProductName(String productName) {
		this.productName = productName;
	}

	/**
	 * @return the applicantTel
	 */
	public String getApplicantTel() {
		return applicantTel;
	}

	/**
	 * @param applicantTel the applicantTel to set
	 */
	public void setApplicantTel(String applicantTel) {
		this.applicantTel = applicantTel;
	}

	/**
	 * @return the loanAmount
	 */
	public String getLoanAmount() {
		return loanAmount;
	}

	/**
	 * @param loanAmount the loanAmount to set
	 */
	public void setLoanAmount(String loanAmount) {
		this.loanAmount = loanAmount;
	}

	/**
	 * @return the loanLimit
	 */
	public String getLoanLimit() {
		return loanLimit;
	}

	/**
	 * @param loanLimit the loanLimit to set
	 */
	public void setLoanLimit(String loanLimit) {
		this.loanLimit = loanLimit;
	}

	/**
	 * @return the company
	 */
	public String getCompany() {
		return company;
	}

	/**
	 * @param company the company to set
	 */
	public void setCompany(String company) {
		this.company = company;
	}

	/**
	 * @return the applicantDate
	 */
	public String getApplicantDate() {
		return applicantDate;
	}

	/**
	 * @param applicantDate the applicantDate to set
	 */
	public void setApplicantDate(String applicantDate) {
		this.applicantDate = applicantDate;
	}

	/**
	 * @return the orderStatus
	 */
	public String getOrderStatus() {
		return orderStatus;
	}

	/**
	 * @param orderStatus the orderStatus to set
	 */
	public void setOrderStatus(String orderStatus) {
		this.orderStatus = orderStatus;
	}

	/**
	 * @return the orderType
	 */
	public String getOrderType() {
		return orderType;
	}

	/**
	 * @param orderType the orderType to set
	 */
	public void setOrderType(String orderType) {
		this.orderType = orderType;
	}
	

}
