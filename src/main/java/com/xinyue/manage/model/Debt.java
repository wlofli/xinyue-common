package com.xinyue.manage.model;

/**
 * 负债信息
 * @author MK)茅
 *
 */
public class Debt {

	
	private String id = "";
	private String rate = "";
	private String collateral = "";
	private String repayIncome = "";
	private String isBig = "";
	private String netAsset = "";
	private String floatRate = "";
	private String factAsset = "";
	/**
	 * @return the rate
	 */
	public String getRate() {
		return rate;
	}
	/**
	 * @param rate the rate to set
	 */
	public void setRate(String rate) {
		this.rate = rate;
	}
	/**
	 * @return the repayIncome
	 */
	public String getRepayIncome() {
		return repayIncome;
	}
	/**
	 * @param repayIncome the repayIncome to set
	 */
	public void setRepayIncome(String repayIncome) {
		this.repayIncome = repayIncome;
	}
	/**
	 * @return the isBig
	 */
	public String getIsBig() {
		return isBig;
	}
	/**
	 * @param isBig the isBig to set
	 */
	public void setIsBig(String isBig) {
		this.isBig = isBig;
	}
	/**
	 * @return the netAsset
	 */
	public String getNetAsset() {
		return netAsset;
	}
	/**
	 * @param netAsset the netAsset to set
	 */
	public void setNetAsset(String netAsset) {
		this.netAsset = netAsset;
	}
	/**
	 * @return the floatRate
	 */
	public String getFloatRate() {
		return floatRate;
	}
	/**
	 * @param floatRate the floatRate to set
	 */
	public void setFloatRate(String floatRate) {
		this.floatRate = floatRate;
	}
	/**
	 * @return the factAsset
	 */
	public String getFactAsset() {
		return factAsset;
	}
	/**
	 * @param factAsset the factAsset to set
	 */
	public void setFactAsset(String factAsset) {
		this.factAsset = factAsset;
	}
	/**
	 * @return the collateral
	 */
	public String getCollateral() {
		return collateral;
	}
	/**
	 * @param collateral the collateral to set
	 */
	public void setCollateral(String collateral) {
		this.collateral = collateral;
	}
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
}
