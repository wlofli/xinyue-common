package com.xinyue.manage.model;

public class Hold {

	private String id ="";
	private String holdType ="";
	private String controlPerson ="";
	private String paperType ="";
	private String paperNumber ="";
	private String workYear ="";
	private String education ="";
	private String marriage ="";
	/**
	 * @return the holdType
	 */
	public String getHoldType() {
		return holdType;
	}
	/**
	 * @param holdType the holdType to set
	 */
	public void setHoldType(String holdType) {
		this.holdType = holdType;
	}
	/**
	 * @return the controlPerson
	 */
	public String getControlPerson() {
		return controlPerson;
	}
	/**
	 * @param controlPerson the controlPerson to set
	 */
	public void setControlPerson(String controlPerson) {
		this.controlPerson = controlPerson;
	}
	/**
	 * @return the paperType
	 */
	public String getPaperType() {
		return paperType;
	}
	/**
	 * @param paperType the paperType to set
	 */
	public void setPaperType(String paperType) {
		this.paperType = paperType;
	}
	/**
	 * @return the paperNumber
	 */
	public String getPaperNumber() {
		return paperNumber;
	}
	/**
	 * @param paperNumber the paperNumber to set
	 */
	public void setPaperNumber(String paperNumber) {
		this.paperNumber = paperNumber;
	}
	/**
	 * @return the workYear
	 */
	public String getWorkYear() {
		return workYear;
	}
	/**
	 * @param workYear the workYear to set
	 */
	public void setWorkYear(String workYear) {
		this.workYear = workYear;
	}
	/**
	 * @return the education
	 */
	public String getEducation() {
		return education;
	}
	/**
	 * @param education the education to set
	 */
	public void setEducation(String education) {
		this.education = education;
	}
	/**
	 * @return the marriage
	 */
	public String getMarriage() {
		return marriage;
	}
	/**
	 * @param marriage the marriage to set
	 */
	public void setMarriage(String marriage) {
		this.marriage = marriage;
	}
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
}
