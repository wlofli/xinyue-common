package com.xinyue.authe;

public class MenuLeft {
	
	
	public String getMenuParentText() {
		return menuParentText;
	}
	public void setMenuParentText(String menuParentText) {
		this.menuParentText = menuParentText;
	}
	public String getMenuText() {
		return menuText;
	}
	public void setMenuText(String menuText) {
		this.menuText = menuText;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getMenuId() {
		return menuId;
	}
	public void setMenuId(String menuId) {
		this.menuId = menuId;
	}
	private String menuParentText;
	private String menuText;
	private String url;
	private String menuId;
}
